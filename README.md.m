# Gestor de Catálogo de Productos

Este proyecto es una aplicación de consola para gestionar un catálogo de productos, permitiendo agregar, eliminar, modificar y listar productos en un archivo.

## Funcionalidades
1. Alta, baja y modificación de productos.
2. Guardado y carga de productos en un archivo de texto.
3. Generación de documentación con Javadoc.

## Instrucciones de Uso
1. Clonar el repositorio.
2. Compilar el proyecto:
    ```
    make compile
    ```
3. Ejecutar el programa:
    ```
    java -cp bin GestorDeCatalogo
    ```

